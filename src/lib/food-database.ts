export function getFoodImage(foodName: string): string {
  const normalizedName = foodName.toLowerCase().trim();

  // Check exact matches first
  if (foodImageFallbacks[normalizedName]) {
    return foodImageFallbacks[normalizedName];
  }

  // Check partial matches
  for (const [key, url] of Object.entries(foodImageFallbacks)) {
    if (normalizedName.includes(key) || key.includes(normalizedName.split(' ')[0])) {
      return url;
    }
  }

  // Fallback to Unsplash search
  const encoded = encodeURIComponent(foodName);
  return `https://source.unsplash.com/400x300/?${encoded},food`;
}

