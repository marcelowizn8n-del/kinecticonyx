export function searchFoods(query: string, category?: FoodCategory): FoodItem[] {
  const lowerQuery = query.toLowerCase().trim();

  return TACO_DATABASE.filter((food) => {
    const matchesQuery =
      food.name.toLowerCase().includes(lowerQuery) ||
      food.id.toLowerCase().includes(lowerQuery);

    const matchesCategory = !category || food.category === category;

    return matchesQuery && matchesCategory;
  });
}

export function getFoodsByCategory(category: FoodCategory): FoodItem[] {
  return TACO_DATABASE.filter((food) => food.category === category);
}

export function calculateMealNutrition(
  foods: { food: FoodItem; quantity: number }[]
): NutritionSummary {
  const summary: NutritionSummary = {
    calories: 0,
    protein: 0,
    carbs: 0,
    fat: 0,
    fiber: 0,
    sodium: 0,
    sugar: 0,
    saturatedFat: 0,
    calcium: 0,
    iron: 0,
    vitaminC: 0,
    vitaminA: 0,
  };

  for (const { food, quantity } of foods) {
    const multiplier = quantity / food.portion;

    summary.calories += food.calories * multiplier;
    summary.protein += food.protein * multiplier;
    summary.carbs += food.carbs * multiplier;
    summary.fat += food.fat * multiplier;
    summary.fiber += food.fiber * multiplier;
    summary.sodium += food.sodium * multiplier;
    summary.sugar += food.sugar * multiplier;
    summary.saturatedFat += food.saturatedFat * multiplier;
    summary.calcium += food.calcium * multiplier;
    summary.iron += food.iron * multiplier;
    summary.vitaminC += food.vitaminC * multiplier;
    summary.vitaminA += food.vitaminA * multiplier;
  }

  // Round to 2 decimal places for cleaner output
  Object.keys(summary).forEach((key) => {
    summary[key as keyof NutritionSummary] = Math.round(summary[key as keyof NutritionSummary] * 100) / 100;
  });

  return summary;
}
